userRouter.post(
  '/:id/reviews',
  isAuth,
  expressAsyncHandler(async (req, res) => {
    const sellerId = req.params.id;
    const seller = await User.findById(req.params.id);
    if (seller) {
      if (seller.seller.reviews.find((x) => x.name === req.user.name)) {
        return res
          .status(400)
          .send({ message: 'You already submitted a review' });
      }
      const review = {
        name: req.user.name,
        rating: Number(req.body.rating),
        comment: req.body.comment,
      };
      seller.seller.reviews.push(review);
      seller.seller.numReviews = seller.seller.reviews.length;
      seller.seller.rating =
        seller.seller.reviews.reduce((a, c) => c.rating + a, 0) /
        seller.seller.reviews.length;
      const updatedSeller = await seller.save();
      res.status(201).send({
        message: 'Review Created',
        review: updatedSeller.seller.reviews[updatedSeller.seller.reviews.length - 1],
      });
    } else {
      res.status(404).send({ message: 'Product Not Found' });
    }
  })
);


